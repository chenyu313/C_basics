//墙壁涂色
/*某同学决定给墙壁涂色，他买了三种颜色，红，黄，蓝，然后把墙壁划分成n个部分，他想每个部分不能相同，一共有
多少种方案(墙壁是一个环形)
输入格式：
		输入一个整数n表示房间被划分成几个部分
输出格式：
		输出总方案数*/
#include<iostream>
using namespace std;
int main()
{
	int n;
	long long a[51];
	a[1]=3;
	a[2]=6;
	a[3]=6;	
	/*
	当n-1与1颜色不同时，第n块只有一种情况：f(n)=f(n-1)*1;
	当n-1与1颜色相同时，第n块有两种情况：f(n)=2*f(n-2); 
	*/
	for(int i=4;i<=50;i++)
	{
		a[i]=a[i-1]+2*a[i-2];
	} 
	cin>>n;
	cout<<a[n]<<endl;
	return 0;
}
